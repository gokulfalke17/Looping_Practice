/*
3. Write a java program to print all alphabets from a to z. - using while loop 
*/

public class AllAlphabates_03 {

	public static void main(String[] agrs) {

		char ch;
		
		System.out.println("Alphabates are ");
		for(ch='a';ch<='z';ch++) {
			System.out.print(ch+" ");
		}
		System.out.println();
		
	}
}
